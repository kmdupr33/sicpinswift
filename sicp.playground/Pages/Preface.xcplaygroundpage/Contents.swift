 # SICP in Swift
 
 The [Structure and Interpretation of Computer Programs](https://mitpress.mit.edu/sicp/full-text/book/book.html) is widely regarded as essential reading for serious programmers. Unfortunately, the programming language used in SICP (viz., scheme) isn't one that's in serious use anywhere. Moreover, the tools avilable for working with scheme are quite limited.
 
 Its unfortunate that the language and tooling can often be an obstacle to learning the concepts expressed in SICP. SICP, after all, is not about a particular programming language:
 
 * callout( ):...we believe that the essential material to be addressed by a subject at this level is not the syntax of particular programming-language constructs, nor clever algorithms for computing particular functions efficiently, nor even the mathematical analysis of algorithms and the foundations of computing, but rather the techniques used to control the intellectual complexity of large software systems.
 
    \- *Abelson et. al.*, pg. 12
 
 This set of playgrounds is an attempt to provide an update for the tooling and language students of SICP need to use in order to grok SICP's concepts. More specifically, it gives students a straight foward way to test whether their answers to the exercises are correct and it gives them the opportunity to learn a language that will likely be more useful to them as professional programmers.
 
 
 
 This set of playgrounds is also an attempt to summarize SICP. This goal is more self-serving than the previous one, as it is unlikely that my summary will retain all of insight and clarity of the the original text. I hope, however, my attempt to summarize the sections of SICP will help solidify its concepts in my own mind, and if you happen to find the summary useful, then that's great. In my summaries, I have tried to include references to the relevant sections of the original text in case you find my summarizing unhelpful. The page numbers mentioned in these references are the pages of [the pdf version of SICP](http://web.mit.edu/alexmv/6.037/sicp.pdf).
 
 
